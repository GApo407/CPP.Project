#include "iostream"
using namespace std;

int main() {
    // 字面常量（整型  实型  字符  字符串）
    // 整型（整数）
    9;
    // 实型（小数）
    9.9;
    // 字符（单个  ' . ' )
    'D';           // 只能提供1个字符  不可以是0个
    // 字符串（多个  " ... " )
    "world";       //   " " 里面可以是单个或者多个 也可以是0个
    cout << 9 << endl;
    cout << 9.9 << endl;
    cout << 'D' << endl;
    cout << "world" << endl;
    return 0;
}